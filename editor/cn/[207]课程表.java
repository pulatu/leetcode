class Solution {
    // æ‹“æ‰‘æ’åºã€dfsã€bfs
   List<List<Integer>> edge = new ArrayList<>();// å‚¨å­˜è¾¹
    int[] visited;
    boolean isVaild = true;
    public boolean canFinish(int numCourses, int[][] prerequisites) {
        // å‚¨å­˜é¡¶ç‚¹çš„çŠ¶æ€(0:æœªæœç´¢ï¼Œ1:æœç´¢ä¸­ï¼Œ2:å·²æœç´¢)
        visited = new int[numCourses];
        for(int i=0;i<numCourses;i++){
            edge.add(new ArrayList<>());
        }
        // è·å–è¾¹çš„çŠ¶æ€
        for (int[] pre : prerequisites) {
            edge.get(pre[1]).add(pre[0]);
        }
        // å¯¹æ¯ä¸ªé¡¶ç‚¹è¿›è¡Œdfs
        for(int i=0;i<numCourses && isVaild;i++){
            visited[i]=1;
            dfs(i);
        }
        return isVaild;
    }
    public void dfs(int u){
        // å¯¹å½“å‰é¡¶ç‚¹çš„æ¯ä¸ªé‚»æ¥ç‚¹è¿›è¡Œæœç´¢
        for (Integer v : edge.get(u)) {
            if(visited[v]==1){
               isVaild = false;
               return;
            }
            else if(visited[v]==0){
                visited[v] = 1;
                dfs(v);
            }
        }
        visited[u] = 2;
        return;
    }

}
